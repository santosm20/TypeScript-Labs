/**
 * Exercício 01 – Função de saudação
 * Objetivo: criar uma função que exibe "Olá, Mundo!" no console.
 */

// Declaração da função chamada 'saudacao'.
// (): indica que a função não recebe parâmetros.
// : void indica que a função não retorna valor (apenas executa efeitos colaterais).
function saudacao(): void {
  // console.log imprime texto no console (terminal).
  // Aqui estamos exibindo a mensagem "Olá, Mundo!".
  console.log("Olá, Mundo!");
}

// Chamada da função: executa o código dentro de 'saudacao'.
// Ao chamar, veremos "Olá, Mundo!" no console.
saudacao();
